#include "libft.h"

void	ft_putllnbr(intmax_t n)
{
	if (n == -9223372036854775807)
	{
		ft_putchar('-');
		ft_putchar('9');
		ft_putllnbr(223372036854775807);
	}
	else
	{
		if (n < 0)
		{
			ft_putchar('-');
			n = -n;
		}
		if (n < 10)
		{
			ft_putchar(n + '0');
		}
		if (n > 9)
		{
			ft_putllnbr(n / 10);
			ft_putchar(n % 10 + '0');
		}
	}
}
